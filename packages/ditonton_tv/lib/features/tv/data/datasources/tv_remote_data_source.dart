        .get(Uri.parse('$baseUrl/tv/$tvId/season/$seasonNumber?$apiKey'));

    if (response.statusCode == 200) {
      return SeasonDetailResponse.fromJson(json.decode(response.body));
    } else {
      throw ServerException();
    }
  }
}
